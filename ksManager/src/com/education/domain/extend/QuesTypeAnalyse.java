package com.education.domain.extend;

public class QuesTypeAnalyse {

	private String quesTypeId;
	private String quesTypeName;
	private String quesCount;
	private String quesSumScore;
	public String getQuesTypeId() {
		return quesTypeId;
	}
	public void setQuesTypeId(String quesTypeId) {
		this.quesTypeId = quesTypeId;
	}
	public String getQuesTypeName() {
		return quesTypeName;
	}
	public void setQuesTypeName(String quesTypeName) {
		this.quesTypeName = quesTypeName;
	}
	public String getQuesCount() {
		return quesCount;
	}
	public void setQuesCount(String quesCount) {
		this.quesCount = quesCount;
	}
	public String getQuesSumScore() {
		return quesSumScore;
	}
	public void setQuesSumScore(String quesSumScore) {
		this.quesSumScore = quesSumScore;
	}
	
	
}
