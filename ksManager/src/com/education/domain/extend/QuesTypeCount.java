package com.education.domain.extend;

public class QuesTypeCount {

	private String typeCode;
	private String typeCodeName;
	private int num;
	private String percentum;
	private String style;
	
	public String getPercentum() {
		return percentum;
	}
	public void setPercentum(String percentum) {
		this.percentum = percentum;
	}
	public String getStyle() {
		return style;
	}
	public void setStyle(String style) {
		this.style = style;
	}
	public String getTypeCodeName() {
		return typeCodeName;
	}
	public void setTypeCodeName(String typeCodeName) {
		this.typeCodeName = typeCodeName;
	}
	public String getTypeCode() {
		return typeCode;
	}
	public void setTypeCode(String typeCode) {
		this.typeCode = typeCode;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
	
}
